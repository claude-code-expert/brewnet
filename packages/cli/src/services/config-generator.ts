/**
 * T068 — Infrastructure Config Generator
 *
 * Generates infrastructure configuration files from WizardState.
 * Produces sshd_config, Gitea app.ini, FileBrowser config, Traefik config,
 * mail server configs (postfix main.cf, dovecot.conf), and supports
 * template variable substitution.
 *
 * Design principles:
 * - Secure by Default: SSH key-only auth, no root login
 * - Transparent: All generated configs are human-readable
 *
 * @module services/config-generator
 */

import type { WizardState, GeneratedFile } from '@brewnet/shared';

// ---------------------------------------------------------------------------
// Template Variable Substitution
// ---------------------------------------------------------------------------

/**
 * Replace template variables in a string with values from WizardState.
 *
 * Supported variables:
 *   - `${PROJECT_NAME}` -> state.projectName
 *   - `${DOMAIN}`       -> state.domain.name
 *   - `${ADMIN_USER}`   -> state.admin.username
 *
 * Unknown variables are left unchanged.
 *
 * @param template - Template string containing `${VAR}` placeholders
 * @param state    - Current wizard state
 * @returns The template with known variables replaced
 */
export function substituteTemplateVars(
  template: string,
  state: WizardState,
): string {
  const vars: Record<string, string> = {
    PROJECT_NAME: state.projectName,
    DOMAIN: state.domain.name,
    ADMIN_USER: state.admin.username,
  };

  return template.replace(/\$\{([A-Z_]+)\}/g, (match, varName: string) => {
    return varName in vars ? vars[varName] : match;
  });
}

// ---------------------------------------------------------------------------
// SSH — sshd_config
// ---------------------------------------------------------------------------

/**
 * Generate an OpenSSH sshd_config based on wizard state.
 *
 * Security defaults (per Brewnet constitution):
 * - PubkeyAuthentication yes (always)
 * - PermitRootLogin no (always)
 * - PasswordAuthentication no (unless explicitly enabled)
 *
 * @param state - Current wizard state
 * @returns GeneratedFile with path and sshd_config content
 */
export function generateSshdConfig(state: WizardState): GeneratedFile {
  const ssh = state.servers.sshServer;
  const passwordAuth = ssh.passwordAuth ? 'yes' : 'no';

  const lines: string[] = [
    '# Brewnet SSH Server Configuration',
    '# Generated by brewnet config-generator',
    '',
    `Port ${ssh.port}`,
    'AddressFamily any',
    'ListenAddress 0.0.0.0',
    'ListenAddress ::',
    '',
    '# Authentication',
    'PermitRootLogin no',
    'PubkeyAuthentication yes',
    `PasswordAuthentication ${passwordAuth}`,
    'ChallengeResponseAuthentication no',
    'UsePAM yes',
    '',
    '# Security',
    'X11Forwarding no',
    'PrintMotd no',
    'AcceptEnv LANG LC_*',
    'MaxAuthTries 3',
    'MaxSessions 10',
    '',
  ];

  // SFTP subsystem
  if (ssh.sftp) {
    lines.push('# SFTP Subsystem');
    lines.push('Subsystem sftp /usr/lib/openssh/sftp-server');
    lines.push('');
  }

  return {
    path: 'infrastructure/ssh/sshd_config',
    content: lines.join('\n'),
  };
}

// ---------------------------------------------------------------------------
// Gitea — app.ini
// ---------------------------------------------------------------------------

/**
 * Generate a Gitea app.ini configuration file.
 *
 * Includes [server], [database], [security] sections with admin
 * username and domain from wizard state.
 *
 * @param state - Current wizard state
 * @returns GeneratedFile with path and app.ini content
 */
export function generateGiteaConfig(state: WizardState): GeneratedFile {
  const git = state.servers.gitServer;
  const domain = state.domain.name;
  const admin = state.admin;

  const lines: string[] = [
    '; Brewnet Gitea Configuration',
    '; Generated by brewnet config-generator',
    '',
    '[server]',
    `DOMAIN = ${domain}`,
    `HTTP_PORT = ${git.port}`,
    `ROOT_URL = http://${domain}:${git.port}/`,
    `SSH_PORT = ${git.sshPort}`,
    `SSH_DOMAIN = ${domain}`,
    'DISABLE_SSH = false',
    'START_SSH_SERVER = true',
    'LFS_START_SERVER = true',
    '',
    '[database]',
    'DB_TYPE = sqlite3',
    'PATH = /data/gitea/gitea.db',
    '',
    '[security]',
    'INSTALL_LOCK = true',
    `INTERNAL_TOKEN = auto`,
    `SECRET_KEY = auto`,
    '',
    '[service]',
    'DISABLE_REGISTRATION = true',
    'REQUIRE_SIGNIN_VIEW = true',
    'DEFAULT_KEEP_EMAIL_PRIVATE = true',
    '',
    '[admin]',
    `ADMIN_NAME = ${admin.username}`,
    '',
    '[log]',
    'MODE = console',
    'LEVEL = info',
    '',
  ];

  return {
    path: 'infrastructure/gitea/app.ini',
    content: lines.join('\n'),
  };
}

// ---------------------------------------------------------------------------
// FileBrowser — filebrowser.json
// ---------------------------------------------------------------------------

/**
 * Generate a FileBrowser configuration file.
 *
 * Returns null if FileBrowser is disabled in the wizard state.
 *
 * @param state - Current wizard state
 * @returns GeneratedFile with filebrowser.json content, or null if disabled
 */
export function generateFileBrowserConfig(
  state: WizardState,
): GeneratedFile | null {
  const fb = state.servers.fileBrowser;

  if (!fb.enabled) {
    return null;
  }

  const config: Record<string, unknown> = {
    port: 80,
    baseURL: '',
    address: '0.0.0.0',
    log: 'stdout',
    database: '/database.db',
    root: fb.mode === 'directory' ? '/srv/data' : '/srv',
    auth: {
      method: 'json',
      header: '',
      recaptcha: {
        host: '',
        key: '',
        secret: '',
      },
    },
  };

  return {
    path: 'infrastructure/filebrowser/filebrowser.json',
    content: JSON.stringify(config, null, 2),
  };
}

// ---------------------------------------------------------------------------
// Traefik — traefik.yml
// ---------------------------------------------------------------------------

/**
 * Generate a Traefik static configuration file.
 *
 * Includes entryPoints for HTTP/HTTPS and Docker provider settings.
 *
 * @param state - Current wizard state
 * @returns GeneratedFile with traefik.yml content
 */
export function generateTraefikConfig(state: WizardState): GeneratedFile {
  const domain = state.domain.name;
  const ssl = state.domain.ssl;

  const lines: string[] = [
    '# Brewnet Traefik Configuration',
    '# Generated by brewnet config-generator',
    '',
    '# Entry Points',
    'entryPoints:',
    '  web:',
    '    address: ":80"',
    '  websecure:',
    '    address: ":443"',
    '',
    '# Docker Provider',
    'providers:',
    '  docker:',
    '    endpoint: "unix:///var/run/docker.sock"',
    '    exposedByDefault: false',
    '    network: brewnet',
    '  file:',
    '    directory: /etc/traefik/dynamic',
    '    watch: true',
    '',
    '# API / Dashboard',
    'api:',
    '  dashboard: true',
    '  insecure: true',
    '',
    '# Logging',
    'log:',
    '  level: INFO',
    '',
  ];

  // Certificate resolver for non-local SSL
  if (ssl === 'letsencrypt') {
    lines.push('# Certificate Resolvers');
    lines.push('certificatesResolvers:');
    lines.push('  letsencrypt:');
    lines.push('    acme:');
    lines.push(`      email: admin@${domain}`);
    lines.push('      storage: /letsencrypt/acme.json');
    lines.push('      httpChallenge:');
    lines.push('        entryPoint: web');
    lines.push('');
  }

  return {
    path: 'infrastructure/traefik/traefik.yml',
    content: lines.join('\n'),
  };
}

// ---------------------------------------------------------------------------
// Mail Server — postfix main.cf + dovecot.conf
// ---------------------------------------------------------------------------

/**
 * Generate mail server configuration files (postfix + dovecot).
 *
 * Returns an empty array if the mail server is disabled.
 * Mail configs are placed under `infrastructure/mail/`.
 *
 * @param state - Current wizard state
 * @returns Array of GeneratedFile for mail server configs
 */
export function generateMailConfig(state: WizardState): GeneratedFile[] {
  const mail = state.servers.mailServer;

  if (!mail.enabled) {
    return [];
  }

  const domain = state.domain.name;
  const files: GeneratedFile[] = [];

  // Postfix main.cf
  const postfixLines: string[] = [
    '# Brewnet Postfix Configuration',
    '# Generated by brewnet config-generator',
    '',
    '# General',
    `myhostname = ${domain}`,
    `mydomain = ${domain}`,
    `myorigin = $mydomain`,
    'mydestination = $myhostname, localhost.$mydomain, localhost, $mydomain',
    '',
    '# Network',
    'inet_interfaces = all',
    'inet_protocols = ipv4',
    '',
    '# TLS',
    'smtpd_tls_cert_file = /etc/ssl/certs/ssl-cert-snakeoil.pem',
    'smtpd_tls_key_file = /etc/ssl/private/ssl-cert-snakeoil.key',
    'smtpd_tls_security_level = may',
    'smtp_tls_security_level = may',
    '',
    '# Authentication',
    'smtpd_sasl_type = dovecot',
    'smtpd_sasl_path = private/auth',
    'smtpd_sasl_auth_enable = yes',
    '',
    '# Restrictions',
    'smtpd_recipient_restrictions = permit_sasl_authenticated, reject_unauth_destination',
    '',
  ];

  files.push({
    path: 'infrastructure/mail/postfix/main.cf',
    content: postfixLines.join('\n'),
  });

  // Dovecot configuration
  const dovecotLines: string[] = [
    '# Brewnet Dovecot Configuration',
    '# Generated by brewnet config-generator',
    '',
    '# Protocols',
    'protocols = imap lmtp',
    '',
    '# Mail location',
    'mail_location = maildir:~/Maildir',
    '',
    '# Authentication',
    'auth_mechanisms = plain login',
    '',
    '# SSL',
    'ssl = required',
    `ssl_cert = </etc/ssl/certs/${domain}.pem`,
    `ssl_key = </etc/ssl/private/${domain}.key`,
    '',
    '# Service configuration',
    'service auth {',
    '  unix_listener /var/spool/postfix/private/auth {',
    '    mode = 0660',
    '    user = postfix',
    '    group = postfix',
    '  }',
    '}',
    '',
    '# Logging',
    'log_path = /var/log/dovecot.log',
    'info_log_path = /var/log/dovecot-info.log',
    '',
  ];

  files.push({
    path: 'infrastructure/mail/dovecot/dovecot.conf',
    content: dovecotLines.join('\n'),
  });

  return files;
}

// ---------------------------------------------------------------------------
// Orchestrator — generateInfraConfigs
// ---------------------------------------------------------------------------

/**
 * Generate all infrastructure configuration files based on wizard state.
 *
 * Orchestrates all individual generators and returns a flat array of
 * GeneratedFile objects. Only includes configs for enabled services.
 *
 * Always included:
 * - Traefik config (web server is always enabled)
 * - Gitea app.ini (git server is always enabled)
 *
 * Conditionally included:
 * - sshd_config (when SSH server is enabled)
 * - filebrowser.json (when FileBrowser is enabled)
 * - Mail configs (when mail server is enabled)
 *
 * @param state - Current wizard state
 * @returns Array of GeneratedFile objects for all enabled infrastructure
 */
export function generateInfraConfigs(state: WizardState): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  // Traefik (always included — web server is required)
  files.push(generateTraefikConfig(state));

  // Gitea (always included — git server is required)
  files.push(generateGiteaConfig(state));

  // SSH (conditional)
  if (state.servers.sshServer.enabled) {
    files.push(generateSshdConfig(state));
  }

  // FileBrowser (conditional)
  const fbConfig = generateFileBrowserConfig(state);
  if (fbConfig) {
    files.push(fbConfig);
  }

  // Mail (conditional)
  const mailConfigs = generateMailConfig(state);
  files.push(...mailConfigs);

  return files;
}
